          <h3>DOK Reading</h3>
          <ul class="uk-list uk-list-bullet">
            <li>
              <h4>Level 1 Recall Reading</h4>
              <p>The reader uses simple skills to retrieve or locate information from the text. The responses require
                only literal understanding of text presented and often consist of verbatim recall from text, or simple
                understanding of a single word or phrase.</p>
            </li>
            <li>
              <h4>Level 2 Skills and Concepts Reading</h4>
              <p>The reader shows initial comprehension and subsequent processing of text or portions of text. The
                reader understands important concepts and literal main ideas.</p>
            </li>
            <li>
              <h4>Level 3 Strategic Thinking Reading</h4>
              <p>The reader applies deep knowledge to go beyond the text to explain, generalize, or connect ideas. The
                reader is able to support their thinking, making references and interpretations from the text or other
                sources.</p>
            </li>
          </ul>
          <h3>DOK Writing</h3>
          <ul class="uk-list uk-list-bullet">
            <li>
              <h4>Level 1 Writing Recall</h4>
              <p>The student writes or recites simple facts and basic ideas.</p>
            </li>
            <li>
              <h4>Level 2 Writing Skills and Concepts</h4>
              <p>The student shows evidence of some mental processing, such as beginning to connect ideas using a simple
                organizational structure. The student is engaged in first draft writing for a limited number of purposes
                and audiences. The student is beginning to connect ideas using a simple organizational structure for
                such things as composing a short, accurate summary.</p>
            </li>
            <li>
              <h4>Level 3 Writing Strategic Thinking</h4>
              <p>The student shows evidence of higher-level mental processing. The student is developing multi-paragraph
                compositions that may include complex sentence structures or demonstrate some synthesis and analysis.
                Revisions are made to the writing to improve precision of language used and to produce a logical
                progression of ideas.</p>
            </li>
          </ul>

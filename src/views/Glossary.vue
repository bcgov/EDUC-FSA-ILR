          <h3>Degree of Difficulty</h3>
          <p>The FSA Item Degree of Difficulty (DIFF; Df) estimate is simply the percentage of students who answer an
            item correctly. In this case, it is also equal to the item mean. An item answered correctly for between 100%
            and 90% of students (i.e., 1.0 to 0.9) is described as <b>Very Easy</b>. Similarly, an item answered
            correctly for between 90% and 70% of students is described as <b>Moderately Easy</b>; between 70% and 50% of
            students is described as <b>Moderately Difficult</b>; between 50% and 30% of students is described as
            <b>Very Difficult</b>; and, between 30% and 0% of students is described as <b>Extremely Difficult</b>.</p>
          <h3>Depth of Knowledge (DOK) / Cognitive levels</h3>
